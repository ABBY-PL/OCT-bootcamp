//Arithmetic calculation
let a = 10;
let b = 5;

//Addition
let sum = a + b;
console.log("This is the sum of a and b:", sum);

//Substraction
let difference = a - b;
console.log("This is the difference of a and b:", difference);

let difference1 = b - a;
console.log("This is the difference of a and b:", difference1);

//Multiplication
let product = a * b;
console.log("This is the product of a and b:", product);

//Division
let division = a / b;
console.log("This is division of a and b:", division);

//Modulo / Remainder
let remainder = 12 % 5;
console.log("The remainder of 12 and 5 is:", remainder);




